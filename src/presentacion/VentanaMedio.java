private JLabel lblTablero[][];

    private void crearCeldas() {
        int x = 60;
        int y = 80;
        int ix = 70;
        int iy = 80;
        lblTablero = new JLabel[6][6];
        for (int i = 0; i < 6; i++) {
            for (int j = 0; j < 6; j++) {
                lblTablero[i][j] = new javax.swing.JLabel();
                lblTablero[i][j].setFont(new java.awt.Font("Tahoma", 0, 48)); // NOI18N
                lblTablero[i][j].setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
                lblTablero[i][j].setBorder(BorderFactory.createLineBorder(Color.yellow));
                lblTablero[i][j].setBounds(x + j * ix, y + i * iy, 60, 70); //inr x=70, y=80
                getContentPane().add(lblTablero[i][j]);
                lblTablero[i][j].setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/20.jpg")));
                //Colocamos un identificador
                lblTablero[i][j].setName("" + i + "," + j);
            }
        }

    }

    private void capturarEventos() {
        //Capturamos eventos
        for (int i = 0; i < 6; i++) {
            for (int j = 0; j < 6; j++) {
                lblTablero[i][j].addMouseListener(getControl());
            }
        }
    }
